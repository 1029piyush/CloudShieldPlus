# ----------------------------
# Severity Calculator
# ----------------------------

def calculate_severity(

    business_impact,

    exploitability,

    exposure,

    confidence

):

    score = (

        business_impact * 0.35 +

        exploitability * 0.30 +

        exposure * 0.20 +

        confidence * 0.15

    )

    if score >= 8.5:
        return "Critical"

    elif score >= 6.5:
        return "High"

    elif score >= 4.5:
        return "Medium"

    return "Low"


# ----------------------------
# Rule Lookup
# ----------------------------

def severity_for_rule(rule_id):

    profile = RULE_PROFILES.get(rule_id)

    if profile is None:
        return "Medium"

    return calculate_severity(

        profile["business_impact"],

        profile["exploitability"],

        profile["exposure"],

        profile["confidence"]

    )


